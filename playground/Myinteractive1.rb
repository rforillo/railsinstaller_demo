x = 1
y = 1
a = ""
b = ""
while y < 11
  while x < 11
    sum = x * y
    b = "#{sum}"
    case b.length
      when 1 
        a = a + "   "
      when 2
        a = a + "  "
      when 3
        a = a + " "
    end
    a = a + b
    x = x + 1
  end
  puts a
  a = ""
  x = 1
  y = y + 1
end

